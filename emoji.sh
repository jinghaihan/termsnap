#!/bin/bash

# Print a section title and wrap emojis every N per line
print_section() {
  local title="$1"
  local list="$2"
  local wrap="${3:-30}"

  printf "%s\n" "$title"
  local i=0
  for e in $list; do
    printf "%s " "$e"
    i=$((i + 1))
    if (( i % wrap == 0 )); then
      printf "\n"
    fi
  done
  # Ensure newline if the last line wasn't complete
  if (( i % wrap != 0 )); then
    printf "\n"
  fi
  printf "\n"
}

